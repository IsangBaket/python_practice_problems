print("THIS PROGRAM WILL PRINT THE VALUE OF A NUMBER WHEN RAISED TO THE SECOND NUMBER")
a = float(input("Input a number: "))
b = float(input("Input another number: "))

c = a**b

print(f"{a} raised to {b} is {c}")
